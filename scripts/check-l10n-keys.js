const en = require('../l10n/en.json')
const zh = require('../l10n/zh_CN.json')
const ek = Object.keys(en.translations)
const zk = Object.keys(zh.translations)
const missingInZh = ek.filter((k) => !(k in zh.translations))
const missingInEn = zk.filter((k) => !(k in en.translations))
const sameAsKey = ek.filter((k) => zh.translations[k] === k)
console.log('en keys:', ek.length)
console.log('zh_CN keys:', zk.length)
console.log('missing in zh_CN:', missingInZh.length, missingInZh.slice(0, 10))
console.log('missing in en:', missingInEn.length, missingInEn.slice(0, 10))
console.log('zh same as key (untranslated):', sameAsKey.length, sameAsKey.slice(0, 10))
